// Helper: tìm sản phẩm theo slug
window.HALOVN_findProduct = function (slug) {
  return window.HALOVN_PRODUCTS.find((p) => p.slug === slug) || null;
};

// Helper: format VND
window.HALOVN_formatPrice = function (n) {
  if (n == null) return '';
  return n.toLocaleString('vi-VN') + 'đ';
};
